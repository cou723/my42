#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*rtn_list;

	rtn_list = (t_list *)malloc(sizeof(t_list));
	if (rtn_list == NULL)
		return (NULL);
	rtn_list->content = content;
	rtn_list->next = NULL;
	return (rtn_list);
}
