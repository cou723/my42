#include "get_next_line_private.h"

char	*ft_strchr_ns(const char *s, int c)
{
	char	char_c;

	if (s == NULL)
		return (NULL);
	char_c = (char)c;
	while (*s != '\0')
	{
		if (*s == char_c)
			return ((char *)s);
		s++;
	}
	if (char_c == '\0')
		return ((char *)s);
	return (NULL);
}
